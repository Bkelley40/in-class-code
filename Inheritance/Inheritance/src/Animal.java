public class Animal { // super
	private String color;
	private String name;

	public void setColor (String color)
	{
		this.color = color;
	}
	
	public String getColor ()
	{
		return this.color;
	}
	
	public void setName (String name)
	{
		this.name = name;
	}
	
	public String getName ()
	{
		return this.name;
	}
}
